RADIO = float(input("Escribe el radio del cilindro: "))
ALTU = float(input("Escribre la altura del cilindro: "))
VOL = 3.141595*(RADIO**2)*ALTU
ARE = 2*3.141592*RADIO*ALTU
print(f"El volumen de un cilindro con radio: {RADIO} y Altura: {ALTU} es de: {VOL}")
print(f"El area de un cilindro con radio: {RADIO} y Altura: {ALTU} es de: {ARE}")
